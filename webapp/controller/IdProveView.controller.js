sap.ui.define(["sap/ui/core/mvc/Controller"], function(Controller) {
	"use strict";
	
	return Controller.extend("IdTry.controller.IdProveView", {
		oUserModel: undefined,
		oAttribute: undefined,
		sUserPath: "/services/userapi/currentUser",
		onInit: function() {
			this.oUserModel = new sap.ui.model.json.JSONModel(this.sUserPath);
			this.oAttribute = new sap.ui.model.json.JSONModel("/services/userapi/attributes");
		},
		doTest: function() {
			//getUserInfo();
			var oUserModel = new sap.ui.model.json.JSONModel(this.sUserPath);
			oUserModel.attachRequestCompleted(function () {
				//console.log("Model updated");
			});
			//var oAttribute = new sap.ui.model.json.JSONModel("/services/userapi/attributes");
			this.getView().setModel(oUserModel, "userapi");
		},
		/**
		 *@memberOf IdTry.controller.IdProveView
		 */
		doClear: function() {
			//This code was generated by the layout editor.
			var oUserModel = this.getView().getModel("userapi");
			if (oUserModel) {
				oUserModel.setData(null);
			}
		},
		/**
		 *@memberOf IdTry.controller.IdProveView
		 */
		doCheckData: function() {
			//This code was generated by the layout editor.
			var oUserModel = this.getView().getModel("userapi");
			if (!oUserModel) {
				//console.log("User model is not defined");
				return;
			}
			//console.log("User data", oUserModel.getData());
		},
		/**
		 *@memberOf IdTry.controller.IdProveView
		 */
		doReload: function() {
			//This code was generated by the layout editor.
			var oUserModel = this.getView().getModel("userapi");
			if (oUserModel) {
				oUserModel.loadData(this.sUserPath);
				return;
			}
		}
	});
});